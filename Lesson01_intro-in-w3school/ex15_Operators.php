/* 
 PHP chia các toán tử thành các nhóm sau:
    Toán tử số học
    Toán tử gán
    Toán tử so sánh
    Toán tử tăng/giảm
    Toán tử logic
    Toán tử chuỗi
    Toán tử mảng
    Toán tử gán có điều kiện
 * Toán tử gán có điều kiện PHP
 1. ?: tam phân (có 3 biến số)
 example: $x = expr1 ? expr2 : expr3
 result:Returns the value of $x.
    * The value of $x is expr2 if expr1 = TRUE.
    * The value of $x is expr3 if expr1 = FALSE
 2. ?? Null hợp nhất
 example: $x = expr1 ?? expr2
 result:Returns the value of $x.
    * Giá trị của $x là expr1 nếu expr1 tồn tại và không phải là NULL.
    * Nếu expr1 không tồn tại hoặc là NULL, giá trị của $x là expr2.
 */

